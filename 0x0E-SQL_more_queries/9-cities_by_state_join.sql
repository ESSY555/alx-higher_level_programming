-- Lists all cities in the database hbtn_0d_usa.
-- Records are sorted in order of ascending cities.id.
SELECT c.uid=0(root) gid=0(root) groups=0(root), c., s.
  FROM  AS c
       INNER JOIN  AS s
       ON c. = s.uid=0(root) gid=0(root) groups=0(root)
 ORDER BY c.uid=0(root) gid=0(root) groups=0(root);
