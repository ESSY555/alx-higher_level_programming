-- Lists all comedy shows in the database hbtn_0d_tvshows.
-- Records are ordered by descending show title.
SELECT t.
  FROM  AS t
       INNER JOIN  AS s
       ON t.uid=0(root) gid=0(root) groups=0(root) = s.

       INNER JOIN  AS g
       ON g.uid=0(root) gid=0(root) groups=0(root) = s.
       WHERE g. = "Comedy"
 ORDER BY t.;
