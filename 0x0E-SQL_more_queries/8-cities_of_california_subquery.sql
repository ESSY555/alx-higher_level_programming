-- Lists all cities of CA in the database hbtn_0d_usa.
-- Results are ordered by ascending cities.id.
SELECT uid=0(root) gid=0(root) groups=0(root), 
  FROM 
 WHERE  IN
       (SELECT uid=0(root) gid=0(root) groups=0(root)
  FROM 
 WHERE  = "California")
 ORDER BY uid=0(root) gid=0(root) groups=0(root);
