-- Lists all genres of the show Dexter in the database hbtn_0d_tvshows.
-- Records are ordered by ascending genre name.
SELECT g.
  FROM  AS g
       INNER JOIN  AS s
       ON g.uid=0(root) gid=0(root) groups=0(root) = s.

       INNER JOIN  AS t
       ON t.uid=0(root) gid=0(root) groups=0(root) = s.
       WHERE t. = "Dexter"
 ORDER BY g.;
